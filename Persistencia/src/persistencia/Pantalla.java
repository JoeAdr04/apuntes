    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int id = (int)identificador.getValue();
        System.out.println(id);
        ArchEmpleado archivo =new ArchEmpleado("src/persistencia/empleados.dat");
        
        JFrame nuevaVentana = new JFrame("Nueva Ventana con Entrada de Texto");
        nuevaVentana.setSize(200, 400);
        nuevaVentana.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        nuevaVentana.setLayout(new FlowLayout()); // Organiza los elementos en fila
        // Crear etiquetas y campos de texto
        JLabel label01 = new JLabel("Paterno:");
        JTextField pat = new JTextField(15);
        JLabel label02 = new JLabel("Materno:");
        JTextField mat = new JTextField(16);
        JLabel label03 = new JLabel("Nombre:");
        JTextField nom = new JTextField(17);
        
        JButton boton = new JButton("Confirmar");
        // Acción del botón
        boton.addActionListener(e -> {
            emple.setPaterno(pat.getText());
            emple.setMaterno(mat.getText());
            emple.setNombre(nom.getText());
            emple.setItem(id);
            try {
                archivo.editar(id, emple);
            } catch (IOException ex) {
                System.out.println("Oucrrio un error durante la adicion");
            } catch (ClassNotFoundException ex){
                System.out.println("Oucrrio un error durante la adicion");
            }
            JOptionPane.showMessageDialog(nuevaVentana,
                    "Se ha editado con exito! ");
            identificador.setValue(0);
            nuevaVentana.dispose();         
        });
        // Agregar los elementos a la ventana
        nuevaVentana.add(label01);
        nuevaVentana.add(pat);
        nuevaVentana.add(label02);
        nuevaVentana.add(mat);
        nuevaVentana.add(label03);
        nuevaVentana.add(nom);
        nuevaVentana.add(boton);
        // Mostrar la ventana
        nuevaVentana.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

