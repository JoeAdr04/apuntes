    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Empleado em=new Empleado((int)txtItem.getValue(),txtPat.getText(),txtMat.getText(),txtNom.getText());
        emple = em;
        ArchEmpleado archivo =new ArchEmpleado("src/persistencia/empleados.dat");
        archivo.crear();
        try{
            archivo.adicionar(em);
        }
        catch(IOException | ClassNotFoundException e){
            System.out.println("error");
        }
        txtPat.setText("");
        txtMat.setText("");
        txtNom.setText("");
        txtItem.setValue((int)txtItem.getValue()+1);
    }//GEN-LAST:event_btnGuardarActionPerformed

